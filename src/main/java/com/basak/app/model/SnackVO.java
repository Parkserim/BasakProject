package com.basak.app.model;

public class SnackVO {

	private int snackID;
	private String snackNAME;
	private String snackIMG;
	private String snackCompany;
	private String snackCategory;
	private int snackCal;
	
	public int getSnackID() {
		return snackID;
	}
	public void setSnackID(int snackID) {
		this.snackID = snackID;
	}
	public String getSnackNAME() {
		return snackNAME;
	}
	public void setSnackNAME(String snackNAME) {
		this.snackNAME = snackNAME;
	}
	public String getSnackIMG() {
		return snackIMG;
	}
	public void setSnackIMG(String snackIMG) {
		this.snackIMG = snackIMG;
	}
	public String getSnackCompany() {
		return snackCompany;
	}
	public void setSnackCompany(String snackCompany) {
		this.snackCompany = snackCompany;
	}
	public String getSnackCategory() {
		return snackCategory;
	}
	public void setSnackCategory(String snackCategory) {
		this.snackCategory = snackCategory;
	}
	public int getSnackCal() {
		return snackCal;
	}
	public void setSnackCal(int snackCal) {
		this.snackCal = snackCal;
	}
}